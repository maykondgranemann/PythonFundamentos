# Procedimento = Não possui retorno
# Função = Existe retorno
# parametro = é a variavel recebida em uma funcao ou procedimento
# argumento = é o dado passado durante a chamda de uma funcao ou procedimento

# soma - 
def somar(numero1, numero2):
    resultado = numero1 + numero2
    print(resultado)

#subtracao
def subt(numero1, numero2):
    resultado = numero1 - numero2
    print(resultado)

#multiplicacao
def mult(numero1, numero2):
    resultado = numero1 * numero2
    print(resultado)

#divisao
def divi(numero1, numero2):
    resultado = numero1 / numero2
    print(resultado)

somar(10, 12)
subt(10, 12)
mult(10, 12)
divi(10, 12)